"""
Task #5 - Prepare Benchmark Evaluation Sets

Prepares four evaluation sets used in the paper:
  1. GSM-Plus (distractor-insertion subset)  — OOD evaluation
  2. GSM-DC                                  — OOD evaluation
  3. Template-distracted GSM8K test set      — in-distribution evaluation (placeholder, depends on #10)
  4. Info-Dense GSM8K subset                 — over-filtering evaluation

Output files:
  gsm_plus_distractor.json
  gsm_dc.json
  template_distracted_test.json  (placeholder)
  info_dense.json
"""

import json
import re
from datasets import load_dataset
from pathlib import Path

OUTPUT_DIR = Path("eval_sets")
OUTPUT_DIR.mkdir(exist_ok=True)

ANSWER_PATTERN = re.compile(r"####\s*(-?[\d,]+(?:\.\d+)?)")


def parse_answer(text: str) -> float | None:
    """Extract numeric answer after ####."""
    m = ANSWER_PATTERN.search(text)
    if m:
        try:
            return float(m.group(1).replace(",", ""))
        except ValueError:
            pass
    return None


# ── 1. GSM-Plus (distractor-insertion subset) ────────────────────────────────

def prepare_gsm_plus(output_path: Path) -> list[dict]:
    """
    Load GSM-Plus from HuggingFace and keep only the distractor-insertion subset.
    GSM-Plus has 8 perturbation types — we want type 'distraction insertion' only.
    Paper: Li et al. 2024 (ACL)
    HuggingFace: qintongli/GSM-Plus
    """
    print("Loading GSM-Plus...")
    ds = load_dataset("qintongli/GSM-Plus", split="test")
    print(f"  Total examples: {len(ds)}")

    # Keep only distractor-insertion perturbation type
    distractor_subset = [
        {
            "id": i,
            "question": ex["question"],
            "answer": ex["answer"],
            "gold_answer": parse_answer(ex["answer"]),
            "perturbation_type": ex.get("perturbation_type", "distraction insertion"),
        }
        for i, ex in enumerate(ds)
        if ex.get("perturbation_type", "") == "distraction insertion"
    ]

    print(f"  Distractor subset: {len(distractor_subset)} examples")

    with open(output_path, "w") as f:
        json.dump(distractor_subset, f, ensure_ascii=False, indent=2)
    print(f"  Saved -> {output_path}")
    return distractor_subset


# ── 2. GSM-DC ────────────────────────────────────────────────────────────────

def prepare_gsm_dc(output_path: Path) -> list[dict]:
    """
    Load GSM-DC from HuggingFace.
    GSM-DC models math problems as DAGs and injects distractor nodes at
    controlled structural positions.
    Paper: Yang et al. 2025 (EMNLP)
    HuggingFace: YMinglai/GSM-DC-Dataset-Sample
    """
    print("Loading GSM-DC...")
    try:
        ds = load_dataset("YMinglai/GSM-DC-Dataset-Sample", split="train")
        records = [
            {
                "id": i,
                "question": " ".join(ex["problem_text"]),
                "solution": " ".join(ex["solution_text"]),
                "gold_answer": float(ex["problem_info"]["final_answer"]),
                "irrelevant_nodes_count": ex["irrelevant_nodes_count"],
            }
            for i, ex in enumerate(ds)
        ]
        print(f"  {len(records)} examples loaded")
    except Exception as e:
        print(f"  [warn] Could not load GSM-DC: {e}")
        print("  Check the exact HuggingFace dataset name from Yang et al. 2025 GitHub.")
        records = []

    with open(output_path, "w") as f:
        json.dump(records, f, ensure_ascii=False, indent=2)
    print(f"  Saved -> {output_path}")
    return records


