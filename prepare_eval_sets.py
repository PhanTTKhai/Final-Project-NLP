# ── 4. Info-Dense GSM8K subset ───────────────────────────────────────────────

def prepare_info_dense(output_path: Path, min_sentences: int = 4) -> list[dict]:
    """
    Filter the GSM8K test split to keep only problems where every sentence
    in the question is used in the solution steps.

    These are 'information-dense' problems — used to test whether distractor
    training causes over-filtering of relevant context.

    A sentence is considered 'used' if at least one of its numbers appears
    in the solution steps.

    min_sentences: minimum number of question sentences required (default 4)
    — ensures problems are long enough to be meaningful for this evaluation.
    """
    print("Loading GSM8K test split for Info-Dense subset...")
    ds = load_dataset("gsm8k", "main", split="test")
    print(f"  Total test examples: {len(ds)}")

    def get_sentences(text: str) -> list[str]:
        return [s.strip() for s in re.split(r"(?<=[.!?])\s+", text.strip()) if s.strip()]

    def get_numbers(text: str) -> set[float]:
        nums = set()
        for m in re.finditer(r"(?<!\w)(\d{1,3}(?:,\d{3})*|\d+)(?:\.\d+)?(?!\w)", text):
            try:
                nums.add(float(m.group(0).replace(",", "")))
            except ValueError:
                pass
        return nums

    def all_sentences_used(question: str, solution: str) -> bool:
        """Return True if every question sentence has at least one number used in the solution."""
        sol_text = solution.split("####")[0]
        sol_numbers = get_numbers(sol_text)
        sentences = get_sentences(question)

        # Skip problems with too few sentences
        if len(sentences) < min_sentences:
            return False

        for sent in sentences:
            sent_numbers = get_numbers(sent)
            if not sent_numbers:
                continue  # sentence has no numbers — skip (e.g. "How many did she sell?")
            if sent_numbers.isdisjoint(sol_numbers):
                return False  # this sentence has numbers not used in solution
        return True

    info_dense = []
    for i, ex in enumerate(ds):
        if all_sentences_used(ex["question"], ex["answer"]):
            info_dense.append({
                "id": i,
                "question": ex["question"],
                "answer": ex["answer"],
                "gold_answer": parse_answer(ex["answer"]),
            })

    print(f"  Info-Dense subset: {len(info_dense)} / {len(ds)} examples")

    with open(output_path, "w") as f:
        json.dump(info_dense, f, ensure_ascii=False, indent=2)
    print(f"  Saved -> {output_path}")
    return info_dense


